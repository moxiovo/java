package com.fengtaisec.variable;

public class ASCIIDemo1 {
    public static void main(String[] args) {
        // 掌握 ASCII编码表的特点
        System.out.println('a' + 10); // 97 + 10 = 107
        System.out.println('A' + 10); // 65 + 10 = 75
        System.out.println('0' + 10); // 48 + 10 = 58 

    }
}
